package polimorfismo_eje_2;

public class calculos 
{
    public int sumar(int a, int b)
    {
        return a + b; 
    }
    
    public int sumar(int a, int b, int c)
    {
        return a + b + c;
    }
    
    public double sumar(double a, double b)
    {
        return a + b; 
    }
    
    public float sumar(float a, float b)
    {
    	return a + b; 
    }
    
    
    
    
    
    
    
    
    
}
